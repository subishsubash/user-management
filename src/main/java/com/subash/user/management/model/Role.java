package com.subash.user.management.model;

/**
 * Enumeration representing the available roles for a user in the system.
 * <p>
 * These roles are typically used for access control and authorization purposes.
 */
public enum Role {
    /**
     * Represents an administrator role with elevated privileges.
     */
    ROLE_ADMIN,

    /**
     * Represents a standard user role with limited access.
     */
    ROLE_USER
}
